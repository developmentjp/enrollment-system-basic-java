    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        try{
            String sql = "SELECT * FROM APPLICANT WHERE LRN = '"+txtLRN.getText().trim()+"'";
            conn = ConnectDB.Connect();
            ps = conn.prepareStatement(sql);
            rs = ps.executeQuery();
            
            if(rs.next()){
                lblLastname.setText(rs.getString("LASTNAME"));
                lblFirstname.setText(rs.getString("FIRSTNAME"));
                lblMiddlename.setText(rs.getString("MIDDLENAME"));
                if(rs.getString("EXAMSCORE") == null){
                    btnUpdate.setEnabled(true);
                    btnEdit.setEnabled(false);
                    txtExamscore.setEnabled(true);
                    btnEnroll.setEnabled(false);
                    btnDrop.setEnabled(false);
                }
                else{
                    txtExamscore.setText(rs.getString("EXAMSCORE"));
                    txtExamscore.setEnabled(false);
                    lblExamresult.setText(rs.getString("EXAMRESULT"));
                    btnUpdate.setEnabled(false);
                    btnEdit.setEnabled(true);
                    btnEnroll.setEnabled(true);
                    btnDrop.setEnabled(true);
                    
                }
                lblStatus.setText(rs.getString("STATUS_CODE"));
                lblEnrollto.setText("1stYear-1stSem");
                btnCancel.setEnabled(true);
                btnSearch.setEnabled(false);
                txtLRN.setEnabled(false);
            }
            else{
                JOptionPane.showMessageDialog(null, "Invalid LRN");
                txtLRN.setText("");
            }
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btnSearchActionPerformed

