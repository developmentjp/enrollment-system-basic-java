    public void gwa(){
        int ctr = 0;
        double ave = 0.0, sgrade = 0.0;
        boolean flag = false;
        String aves;
        try{
            String sql = "SELECT SUBJECTGRADE FROM GRADES WHERE STUDENTNUMBER = '"
                    +txtStudentno.getText()+"' AND STATUS_CODE = '"
                    +SStatus.getText()+"'";
            conn = ConnectDB.Connect();
            ps = conn.prepareStatement(sql);
            rs = ps.executeQuery();
            while(rs.next()){
                if(rs.getString("SUBJECTGRADE") == null){
                    flag = true;
                }
                else{
                    sgrade = Double.parseDouble(rs.getString("SUBJECTGRADE"));
                    ave += sgrade;
                    ctr++;
                }
            }
            if(flag == true){
                lblCompleted.setText("Incomplete Grades");
            }
            else{
                SEnroll.setEnabled(true);
                SRemove.setEnabled(true);
                lblCompleted.setText("Complete Grades");
            }
            ave = Math.round((ave/ctr) * 100.0)/100.0;
            aves = Double.toString(ave);
            SGWA.setText(aves);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
